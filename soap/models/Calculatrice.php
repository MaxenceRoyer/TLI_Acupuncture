<?php
	class Calculatrice {
		
		function addition($nombre_1, $nombre_2) {
			return $nombre_1 + $nombre_2;
		}
		
		function soustraction($nombre_1, $nombre_2) {
			return $nombre_1 - $nombre_2;
		}
		
		function multiplication($nombre_1, $nombre_2) {
			return $nombre_1 * $nombre_2;
		}
		
		function division($nombre_1, $nombre_2) {
			return $nombre_1 / $nombre_2;
		}
		
	}
?>